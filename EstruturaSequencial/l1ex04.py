''' 4) Faça um Programa que peça as 4 notas bimestrais e mostre a média. '''

n1 = float(input("Entre com a 1a nota bimestral: "))
n2 = float(input("Entre com a 2a nota bimestral: "))
n3 = float(input("Entre com a 3a nota bimestral: "))
n4 = float(input("Entre com a 4a nota bimestral: ")) 

media = (n1 + n2 + n3 + n4) / 4

print("Média das notas %.02f" % media)